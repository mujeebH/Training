package Java;

public class Assgn2_ {

	public static void main(String[] args) {

		for(int i=0;i<5;i++) {
			int j=1;
			char c='A',s='@';
			for(int k=0;k<5;j++) {
				if(i==k) {
					System.out.print(s+ " ");
				}
				else if(i<=k) {
					System.out.print(c+++ " ");
				}
				else {
					System.out.print(j+++" ");
				}
				
			}
			System.out.println();
			}
	}

	}

